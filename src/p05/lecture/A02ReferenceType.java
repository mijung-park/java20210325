package p05.lecture;

public class A02ReferenceType {
	public static void main(String[] args) {
		int i;
		i = 3;
		
		String s;
		s = new String("java");
		// s 는 사실 참조값을 가진 변수이지만 그냥 s객체라고 표현함
		
		// String : 타입
		// s : (참조)변수
		// new String("java") : 객체(instance or object)
		// s가 가진 값 : 참조값
	}
}
