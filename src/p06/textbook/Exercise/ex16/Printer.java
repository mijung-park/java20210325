package p06.textbook.Exercise.ex16;

public class Printer {

	void println(int a) {
		System.out.println(a);
	}
	
	void println(boolean a) {
		System.out.println(a);
	}
	
	void println(double a) {
		System.out.println(a);
	}
	
	void println(String a) {
		System.out.println(a);
	}

	public void println(String a, String b) {
		System.out.println(a);
		System.out.println(b);
		
	}
}
